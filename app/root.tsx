import { cssBundleHref } from "@remix-run/css-bundle";
import type { LinksFunction } from "@remix-run/node";
import {
  Links,
  LiveReload,
  Meta,
  Outlet,
  Scripts,
  ScrollRestoration,
} from "@remix-run/react";
import globalStyles from './styles/global.css'

export const links: LinksFunction = () => {
  const globalStylesObj = { rel: "stylesheet", href: globalStyles };
  if (cssBundleHref) {
    return [{ rel: "stylesheet", href: cssBundleHref }, globalStylesObj];
  }
  return [globalStylesObj];
}

export default function App() {
  return (
    <html lang="en">
      <head>
        <meta charSet="utf-8" />
        <meta name="viewport" content="width=device-width,initial-scale=1" />
        <Meta />
        <Links />
      </head>
      <body>
        <Outlet />
        <ScrollRestoration />
        <Scripts />
        <LiveReload />
      </body>
    </html>
  );
}
